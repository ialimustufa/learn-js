// Function to change the content of the paragraph
function changeContent() {
document.getElementById("demo").innerHTML = "Content changed!";
}

// Function to change the style of the paragraph
function changeStyle() {
document.getElementById("demo").style.color = "blue";
document.getElementById("demo").style.fontSize = "20px";
}

// Function to change the attribute of the paragraph
function changeAttribute() {
document.getElementById("demo").setAttribute("class", "highlight");
}